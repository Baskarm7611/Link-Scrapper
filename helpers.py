async def write_domain_to_file(filename, domain):
    with open(filename, "w") as f:
        f.write(domain)
    return domain


async def validate_domain(domain):
    return bool(
        re.match(r"^[a-zA-Z0-9][a-zA-Z0-9-]{1,61}[a-zA-Z0-9]\.[a-zA-Z]{2,}$", domain)
    )


async def get_domain(url):
    try:
        return url.split("/")[2]
    except IndexError:
        return url


async def get_domain_from_file(filename="log.txt"):
    with open(filename, "r") as f:
        domain = f.read().strip()
    return domain


async def filter_links_by_domain(links):
    domain = await get_domain_from_file("log.txt")

    return (
        [link for link in links if await get_domain(link) == domain]
        if domain
        else links
    )
