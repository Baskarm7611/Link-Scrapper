import json
import re
from pyrogram import types
import requests
from bs4 import BeautifulSoup


async def scrap_links(url):
    html_page = requests.get(url).text
    soup = BeautifulSoup(html_page, "html.parser")
    links = soup.find_all("a")
    return [link.get("href") for link in links]


async def get_inline_keyboard_markup_url(message: types.Message, links: list):
    if message.reply_markup:
        reply_markup = json.loads(str(message.reply_markup))
        for markup in reply_markup["inline_keyboard"]:
            links.extend(button_data["url"] for button_data in markup)
    return links


